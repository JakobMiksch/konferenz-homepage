        <h2>Samstag, den 14.03.2020</h2>

                <p>Am Samstag, den 14. März 2020 findet Unkonferenz <em>OSM-Samstag</em> der OpenStreetMap-Community statt. Interessierte sind eingeladen daran teilzunehmen. Die Veranstaltung ist kostenfrei. Die Themensammlung erfolgt im <a href="https://wiki.openstreetmap.org/wiki/FOSSGIS_2020/OSM-Samstag">OSM-Wiki</a>. Um 09:30 Uhr werden in der Eröffnungssession die Themen in Zeiten und Räume sortiert. Das Programm besteht voraussichtlich aus je 60 Minuten langen Diskussionrunden, von denen mehrere parallel stattfinden. Für das leibliche Wohl wird gesorgt sein. Die Veranstaltung wird gegen 17:30 Uhr enden.</p>
