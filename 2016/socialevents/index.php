				<h3>Dienstag, 05. Juli 2016</h3>
				<h4>Abendveranstaltung "Dialoge am Amphitheater"</h4>
				<p>Geselliges Beisammensein auf der Terasse des Gebäudes Nawi in der Universität Salzburg. Die "Dialoge am Amphitheater" finden von 18 bis 23 Uhr zusammen mit dem "Icebreaker" der AGIT statt. Die Veranstaltung wird sich durch ein offenes Ambiente auszeichnen. Der Eintritt ist im FOSSGIS-Konferenz-Ticket enthalten. Bei der Teilnahmeanmeldung bitten wir um Angabe der Teilnahme (ja|nein) zum Social Event, dies dient der guten Planung der Veranstaltung.
Als Schlechtwettervariante steht das Foyer der Universität zur Verfügung.</p>

