		<h4>Anreise mit öffentlichen Verkehrsmitteln</h4>
        	<p>Der Hauptbahnhof Dresden liegt ca. 3 Gehminuten vom Hauptgebäude der HTW Dresden entfernt. Dieser wird durch die Deutsche Bahn und trilex bedient.</p>
			<p><strong>Fernverkehrsverbindungen: (Stand 2018)</strong>
 				<ul class="list">
					<li>per ICE direkt ab Wiesbaden über Frankfurt/Main, Erfurt und Leipzig im 2h-Takt</li>
					<li>per Eurocity (EC) ab Hamburg über Berlin im 2h-Takt (Gegenrichtung von Prag kommend)</li>
					<li>weitere IC-/Regionalverbindungen ab Leipzig (von dort zahlreiche Anbindungen im ICE/IC-Netz)</li>
				</ul></p>

		

			<p><strong>Öffentlicher Nahverkehr:</strong>
	Der Zweite große Bahnhof in Dresden ist der Bahnhof Neustadt und 4 km vom Konferenzstandort entfernt. Zum Hauptbahnhof gelangt man mit der S-Bahn 1 und 2 sowie RE15, RE18 sowie trilex.
Neben S-Bahn und Regionalexpress sind viele Orte sehr gut per Straßenbahn (Tram) zu erreichen. Zur HTW fahren die Linien 3, 8 und 11. <br>
 			
Der öffentliche Nahverkehr wird durch die Dresdner Verkehrsbetriebe (DVB) organisiert. Der Ticketkauf kann an den meisten Haltestellen an Automaten erfolgen (teilweise nur Bargeld, größere Haltestellen per EC/VISA). Für Fahrten im Stadtgebiet (inkl. S-Bahn und Fahrten zum Flughafen) genügt ein Fahrschein der Preisstufe 1 bzw. für eine Tarifzone (Einzelfahrt derzeit 2,40€). Bei einem längeren Aufenthalt in Dresden sind entweder 4er-Streifen (8,60€), Tageskarten (6,00€ - gültig bis 4:00 Uhr des Folgetages) oder eine Wochenkarte (21,90€) sinnvoll.</p>  

