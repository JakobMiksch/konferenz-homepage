		<h4>Anreise mit dem Flugzeug</h4>
		  	<p>Die Anreise per Flugzeug ist möglich. Derzeit bestehen neben vielen weiteren Städten Direktverbindungen nach "Dresden International" (DRS) aus Amsterdam, Düsseldorf, Köln/Bonn, Frankfurt, Stuttgart, München, Basel und Zürich. (Stand: Sommerflugplan 2018).<br>
Der Anschluss in die Innenstadt gelingt per Bus+Straßenbahn (Bus 77 direkt ab Flughafen, Umstieg in die Tram 7 ab "Infineon Nord") und S-Bahn. Insbesondere die Anbindung per S-Bahn Linie S2 (Einstieg im Untergeschoss des Flughafens) ist mit 20min Fahrtzeit bis Hauptbahnhof Dresden und einem halbstündigen Takt sehr gut organisiert. Der Flughafen liegt im innerstädtischen Tarifgebiet, so dass hier die regulären Fahrtkosten von derzeit 2,40€ anfallen.  </p>
