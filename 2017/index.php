            <h3>Was ist die FOSSGIS Konferenz?</h3>

            <p>Ziel der jährlich stattfindenden Konferenz ist die Verbreitung von Freier und Open Source Software für Geoinformationssysteme. Hier treffen sich Anwender und Entwickler zum gemeinsamen Austausch über Anwendungs- und Arbeitsmöglichkeiten sowie neuste Entwicklungen in diesem Bereich. Es geht um Web-Kartenanwendungen, Geodateninfrastrukturen, Geodatenmanagement, GeoProcessing und Desktop-GIS.</p>

            <p>Zusätzlich ist sind auch freie Geodaten ein Schwerpunkt der Konferenz. Spezielles Augenmerk liegt hier auf dem Projekt OpenStreetMap.</p>

            <h3>An welchen Orten wird die Konferenz ausgetragen?</h3>
            <p>Typischerweise wird die Konferenz an Hochschulen oder Bildungseinrichtungen durchgeführt.</p>

            <p>In den letzten Jahren war die Konferenz zu Gast bei der Hochschule Anhalt in Dessau (2012), bei der HSR Hochschule für Technik in Rapperswil (2013) und bei der Beuth Hochschule für Technik in Berlin.</p>

            <p>Zuletzt gastierte die Konferenz an der Westfälische Wilhelms-Universität in Münster. Die nächste Konferenz findet im Jahr 2016 erstmalig in Salzburg statt.</p>

            <h3>Wie finanziert sich die Konferenz?</h3>

            <p>Die Konferenz wird vorwiegend durch ehrenamtliches Engagement getragen, wobei neben den Teilnehmergebühren, die Spenden- und vor allem die Sponsoren- und Ausstellerstandeinnahmen eine wichtige finanzielle Einnahmequelle darstellen, um die Kosten für die Konferenz zu decken und darüber hinaus Open Source Projekte zu fördern.</p>

