Für Reisende aus der Richtung München empfehlen wir dringend die Anreise über die A92/A3 über Deggendorf und nicht die A94 zu verwenden.<br>
Während der Konferenzzeit wird die Tiefgarage unter der Zentralbibliothek kostenfrei für alle Teilnehmer zur Verfügung stehen. Zu beachten ist, dass die Tiefgarage Nachts abgesperrt wird. <br><br>
