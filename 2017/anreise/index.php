				<h4>Anreise mit öffentlichen Verkehrsmitteln</h4>
                <p>Passau besitzt einen ICE-Bahnhof und ist somit über den Fernverkehr gut erreichbar. Vom Hauptbahnhof Passau sind es nur wenige hundert Meter zur Universität sowie zu vielen stadtgelegenen Unterkünften.<br>
Die Buslinien 8 und 9 verkehren zwischen Hauptbahnhof und Universität und passieren den ZOB (Zentraler Omnibus Bahnhof). Die Haltestellen Universität und Wörthstraße sind in unmittelbarer Nähe zum Veranstaltungsort.<br>
Weitere Informationen finden sich auf den Seiten der <a href="https://www.stadtwerke-passau.de/bus-parken/busfahrplaene.html">Stadtwerke Passau</a>.<br></p>


