	<h2> Call for Participation für die FOSSGIS-Konferenz 2021 ist geöffnet</h2>

	<p class="text-right"><a class="btn btn-primary" href="href="https://pretalx.com/fossgis2021/">&rarr;hier geht es direkt zur Einreichung&rarr;</a></p>

    <p>Die FOSSGIS-Konferenz ist die größte deutschsprachige Konferenz für OpenStreetMap und Open-Source-Software für Geoinformationen und wird im Jahr 2021 zum 15. Mal veranstaltet.
Konferenzveranstalter sind der gemeinnützige FOSSGIS e.V., die OpenStreetMap-Community und die Ostschweizer Fachhochschule in Rapperswil.</p>

<p>Die FOSSGIS-Konferenz ist eine Community-Konferenz und Plattform für neue Ideen, Projekte und Erfahrungsberichte. Sie wird größtenteils ehrenamtlich organisiert und getragen. Die Veranstaltung fördert die Wissensvermittlung zu Freier und Open-Source-Software für Geoinformationssysteme (FOSSGIS) und treibt so die Verbreitung dieser Software voran.</p>

<p>Am Sonntag, den 6. Juni startet die FOSSGIS-Konferenz mit dem “OSM-Sonntag”, einer Unkonferenz der deutschsprachigen OpenStreetMap-Community. Schwerpunkt sind längere Vorträge und Diskussionsrunden zu den aktuellen Themen im OpenStreetMap-Projekt.</p>

<p>Die Konferenz findet vom Montag, dem 7. Juni bis Mittwoch, dem 9. Juni 2021 mit Vorträgen, Workshops, Demo-Sessions und Anwendertreffen in Rapperswil und gleichzeitig als Online-Event statt. An diesen vier Tagen treffen sich Anwender:innen und Entwickler:innen zum gemeinsamen Austausch über Anwendungs- und Einsatzmöglichkeiten und diskutieren neuste Entwicklungen. Die gleichzeitig stattfindende Firmen- und Projektausstellung ermöglicht einen direkten Kontakt zu Dienstleistern und Projekten.</p>

    <p>Der Call for Participation läuft bis zum 7. Februar 2021.</p>

    <h2>Themenbereiche</h2>

	<p>Die FOSSGIS-Konferenz bedient ein weites Themenfeld, welches unter anderem die folgenden Aspekte umfasst:</p>
	<ul>
    	<li>Grundlagen Open-Source-GIS und OpenStreetMap</li>
    	<li>OpenStreetMap</li>
    	<li>Neuigkeiten aus den Open-Source-Projekten</li>
    	<li>Praxisberichte</li>
    	<li>Daten und Datenprozessierung</li>
    	<li>Routing und Mobilität</li>
    	<li>Rasterdaten und Fernerkundung</li>
    	<li>3D, Drohnen, LIDAR</li>
    	<li>Kartographie und Visualisierung</li>
    	<li>Offene Standards, z.B. INSPIRE, OGC</li>
    	<li>Open Data und Lizenzen</li>
    	<li>Ausbildung und Lehre</li>
     </ul>

  	<h2>Einreichungstypen</h2>

	<p>Das Konferenzprogramm ergibt sich aus unterschiedlichen Formaten:</p>
	<ul>
		<li><strong>Vortrag</strong><br>
Ein Vortrag ist 20 Minuten lang. Danach sind 5 Minuten Zeit für Fragen. Vorträge bilden den Hauptteil der Konferenz.</li>
		
		<li><strong>Langer Vortrag</strong><br>
Ein langer Vortrag dauert 40 Minuten. Es sind danach 15 Minuten für die Fragerunde vorgesehen. Bei der Einreichung ist anzugeben, warum der Vortrag für 20 Minuten zu kurz wäre.</li>

		<li><strong>Workshop</strong><br>
Workshops dauern 90 Minuten und finden von Montag bis Mittwoch parallel zu den Vorträgen statt. Die Teilnahme am Workshop ist kostenpflichtig, die Teilnehmer:innenzahl begrenzt. 
Workshops mit zwei Betreuenden sind möglich, insbesondere, wenn der Workshop stark nachgefragt ist. 
Workshops können sowohl vor Ort als auch Remote stattfinden.</li>

		<li><strong>Demo-Sessions</strong><br>
Demo-Sessions sind 30 oder 60 Minuten lang und dienen der Vorführung von Software oder Prozessen ("Live-Hacking").</li>

		<li><strong>Lightning Talk</strong><br>
Lightning Talks dauern 5 Minuten und sind eine Gelegenheit in Kürze neue Software, neue Projekte oder neue Ideen zu präsentieren. Sie können auch noch kurz vor und während der Konferenz eingereicht werden, sofern Kapazitäten verfügbar sind.</li>

		<li><strong>BoF / Anwendertreffen</strong><br>
Anwendertreffen oder "Birds of a Feather"-Sessions (BoF) ermöglichen es Gleichgesinnten und -interessierten sich konzentriert und selbstorganisiert zu einem bestimmten Thema auszutauschen. BoFs können 30 bis 90 Minuten lang sein und sind als spontanes Format gedacht. Sie können insbesondere auch noch kurz vor und während der Konferenz angemeldet werden.</li>

		<li><strong>Sonstiges</strong><br>
Ihre Einreichung passt nicht zu einem der Einreichungstypen? Dann setzen Sie sich bitte vor dem Ende der Einreichungsfrist mit dem Programm-Komitee per E-Mail in Verbindung (pko@fossgis.de).</li>

	</ul>


	<h2>Bewertungskriterien</h2>

	<p>Bei der Bewertung der Einreichungen werden die folgenden Kriterien angewendet:</p>
		<ul>
			<li>Die Inhalte der Konferenz dienen der Wissensvermittlung und dem Erfahrungsaustausch bezüglich aktueller Themen.</li>

			<li>Bei der Einreichung muss es um freie Software oder freie Daten gehen.
				<ul>
				<li>Software ist frei, wenn ihre Lizenzbedingungen von der [Free Software Foundation](https://www.gnu.org/licenses/license-list.html) als frei anerkannt werden und der Quellcode öffentlich verfügbar ist.</li>
				<li>Daten sind frei, wenn ihre Lizenzbedingungen der [Open Definition](https://opendefinition.org/) entsprechen.</li>
				<li>Der FOSSGIS e.V. unterstützt die Initiative [Public Money, Public Code](https://publiccode.eu/de/). Wir ermuntern alle Projekte und Organisationen, die mit öffentlichen Mitteln freie Software finanzieren, sich und ihre Projekte / Software vorzustellen und von ihren Erfahrungen zu berichten.</li>
				<li>Selbstverständlich freuen wir uns über Beiträge, die die Migration von unfreier zu freier Software oder die Nutzung proprietärer Formate/Schnittstellen mit freier Software thematisieren.</li>
				</ul>
			<li>Vorträge sollten einen Neuigkeitswert bieten. Vorträge im Themenbereich "Grundlagen" sind hiervon ausgenommen.</li>
			<li>Eine Einreichung muss von einer oder mehreren natürlichen Personen eingereicht werden.</li>
			<li>Einreichungen werden nach inhaltlichen Kriterien bewertet. Daher ist eine aussagekräftige Beschreibung wichtig. </li>
			<li>Beiträge von Sponsoren werden nicht bevorzugt behandelt, die Vorträge dienen nicht der reinen Firmenpräsentation.</li>
			<li>Mitglieder des Programm-Komitees bewerten nicht ihre eigenen Beiträge sowie die ihrer Kollegen und Verwandten.</li>
			<li>Erstmals wird ein Community-Voting stattfinden. Das Ergebnis wird vom Programm-Komitee als Stimmungsbild aus der Community einbezogen.</li>
			
			<h3>Zusätzlich für Workshops:</h3>
			<li>Wir bevorzugen Workshops zu aktuellen und nachgefragten Themen.</li>
			<li>Die geschätzte Nachfrage auf Basis der Vorjahre hat einen erheblichen Einfluss auf die Auswahl.</li>
	</ul>

	<h2>Grundsätze der Programmgestaltung</h2>

	<p>Wir sind bestrebt ein ausgewogenes und informatives Programm anzubieten.</p>

	<p>Neue Vortragende sind willkommen! Wir wünschen uns in jeder Hinsicht Vielfalt unter den Vortragenden. Daher freuen wir uns besonders über Einreichungen von Personen, die noch nicht oder noch nicht oft vorgetragen haben. Sollten Sie sich unsicher sein, zögern Sie nicht das Programm-Komitee zu kontaktieren (<a href="mailto:pko@fossgis.de">E-Mail</a>, Telefon: +49 30-62932037)</p>

	<p>Die Konferenzsprache ist Deutsch. In Ausnahmefällen können Beiträge auf Englisch erfolgen.</p>

	<p>Idealerweise sollte kein:e Redner:in mit mehr als zwei bis drei Beiträgen auf der Konferenz vertreten sein. Eingereicht werden dürfen dennoch gerne mehr als drei Beiträge.</p>

	<p>Falls Themen doppelt eingereicht werden, bemühen wir uns, gemeinsam mit den Einreichenden eine Lösung zu finden.</p>

	<p>Das Programm wird so arrangiert, dass thematisch zusammenpassende Beiträge nacheinander im selben Raum stattfinden. Beiträge werden zeitlich stets so platziert, dass Vortragende nie zwei Vorträge in verschiedenen Räumen mit weniger als 30 Minuten Pause dazwischen haben.</p>


