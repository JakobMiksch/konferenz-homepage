	<h2>Hybride Veranstaltung, Streaming und Aufzeichnung</h2>

	<p>Die Konferenz wird als hybride Veranstaltung ausgerichtet. Sie können sowohl vor Ort als auch aus der Ferne (remote) teilnehmen. Aus diesem Grund ist es erforderlich, dass alle Vortragenden dem Streaming und der Aufzeichnung ihrer Beiträge zustimmen.</p>

	<p>Sollte die Infektionslage der Durchführung einer Konferenz vor Ort im Wege stehen, wird die FOSSGIS eine reine Online-Konferenz werden. Um Programmänderungen zu vermeiden, erwarten wir daher von allen Beitragenden, in solch einem Fall aus der Ferne vorzutragen.
Sollten Sie remote teilnehmen, haben Sie die Möglichkeit den Vortrag live vorzutragen oder vorab aufzuzeichnen. Im Falle der Vorab-Aufzeichnung gehen wir davon aus, dass Sie während der Vortragszeit anwesend sind und für Fragen zur Verfügung stehen.</p>

	<p>Wir bitten Sie bei der Einreichung anzugeben, ob Sie planen vor Ort oder aus der Ferne teilzunehmen. Sollten Sie remote teilnehmen, geben Sie bitte an, ob Sie den Vortrag live oder vorab aufgezeichnet präsentieren möchten.</p>



